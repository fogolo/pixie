def km_to_miles(km):
    return km * 1.609344
kilom =float(input("write ur km that u want to transfer to km here:"))
mile=km_to_miles(kilom)
print(f"{kilom} km is equal to {mile:.2f} miles ")
def kg_to_pounds(kg):
    return kg * 2.20462
kg = float(input("write ur kg that u want to transfer to km here:"))
pound = kg_to_pounds(kg)
print(f"{kg} kg is equal to {pound:.2f} pounds ")